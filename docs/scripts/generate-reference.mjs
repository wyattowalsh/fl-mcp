import { promises as fs } from 'node:fs';
import path from 'node:path';

const rootDir = path.resolve(process.cwd(), '..');
const docsDir = path.resolve(process.cwd(), 'content/docs/reference/generated');

async function walk(dir, predicate, acc = []) {
  let entries = [];
  try {
    entries = await fs.readdir(dir, { withFileTypes: true });
  } catch {
    return acc;
  }

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      await walk(fullPath, predicate, acc);
      continue;
    }

    if (predicate(fullPath)) {
      acc.push(fullPath);
    }
  }

  return acc;
}

async function generateSchemaInventory() {
  const schemaFiles = await walk(
    path.join(rootDir, 'schemas'),
    (filePath) => filePath.endsWith('.json')
  );

  const rows = schemaFiles
    .sort()
    .map((filePath) => {
      const rel = path.relative(rootDir, filePath);
      return `- \`${rel}\``;
    });

  const body = `---\ntitle: Generated Schema Inventory\ndescription: Auto-generated list of schema files in the repository.\n---\n\n` +
    `> This file is generated by \`docs/scripts/generate-reference.mjs\`.\n\n` +
    (rows.length ? rows.join('\n') : '_No schema files found under `schemas/`._') +
    '\n';

  await fs.writeFile(path.join(docsDir, 'schema-inventory.mdx'), body, 'utf8');
}

function extractExports(source) {
  const symbols = new Set();
  const named = source.matchAll(/export\s+(?:const|function|class|type|interface|enum)\s+([A-Za-z0-9_]+)/g);
  for (const match of named) {
    symbols.add(match[1]);
  }

  const listed = source.matchAll(/export\s*\{([^}]+)\}/g);
  for (const match of listed) {
    match[1]
      .split(',')
      .map((item) => item.trim().split(/\s+as\s+/i)[1] ?? item.trim().split(/\s+as\s+/i)[0])
      .filter(Boolean)
      .forEach((item) => symbols.add(item));
  }

  return [...symbols].sort();
}

async function generateApiInventory() {
  const apiFiles = await walk(
    path.join(rootDir, 'src'),
    (filePath) => /\.(ts|tsx|js|mjs|cjs)$/.test(filePath)
  );

  const rows = [];
  for (const filePath of apiFiles.sort()) {
    const rel = path.relative(rootDir, filePath);
    const source = await fs.readFile(filePath, 'utf8');
    const exports = extractExports(source);
    if (!exports.length) continue;

    rows.push(`### \`${rel}\``);
    rows.push('');
    rows.push(...exports.map((symbol) => `- \`${symbol}\``));
    rows.push('');
  }

  const body = `---\ntitle: Generated Public API Inventory\ndescription: Auto-generated exported symbol index from the source tree.\n---\n\n` +
    `> This file is generated by \`docs/scripts/generate-reference.mjs\`.\n\n` +
    (rows.length ? rows.join('\n') : '_No exported symbols found under `src/`._') +
    '\n';

  await fs.writeFile(path.join(docsDir, 'api-inventory.mdx'), body, 'utf8');
}

await fs.mkdir(docsDir, { recursive: true });
await Promise.all([generateSchemaInventory(), generateApiInventory()]);
console.log('Generated schema and API inventory docs.');
